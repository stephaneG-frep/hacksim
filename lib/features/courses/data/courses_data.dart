    CourseQuizQuestion(
      prompt: 'Pourquoi la sensibilisation est-elle importante ?',
      options: const [
        'Parce que les attaques réelles n’existent pas',
        'Parce que la plupart des incidents commencent par une erreur humaine évitable',
